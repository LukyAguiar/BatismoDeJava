package NivelIntermediario;

public class Ninja {

    String nome;
    String aldeia;
    int idade;

    public int getIdade(){
        return idade;
    }

    public String getNome(){
        return nome;
    }

    public String getAldeia(){
        return aldeia;
    }

    public void setIdade(int idade){
        this.idade = idade;
    }

    public void setNome(String nome){
        this.nome = nome;
    }

    public void setAldeia(String aldeia){
        this.aldeia = aldeia;
    }
}
